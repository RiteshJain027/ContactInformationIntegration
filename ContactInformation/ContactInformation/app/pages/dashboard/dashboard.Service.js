/*****************************************************************************************
dashboardService Start
******************************************************************************************/
(function () {
    'use strict';

	angular.module('BlurAdmin.services').
        service('dashboardService', dashboardService);

    /*****************************************************************************************
    commonService Start
    ******************************************************************************************/
    function dashboardService($http) {

        this.applyClaim = applyClaim;
        this.releaseClaim = releaseClaim;

        /**********************************************************************
        Function applyClaim Start
        **********************************************************************/
        function applyClaim(loanId, userId) {
            var config = {
                LoanId: loanId,
                UserId: userId
            }
            return $http.post(INSERT_CLAIM_URL, config).then(function (response) {
                return response.data;
            });
        };
        /**********************************************************************
        Function applyClaim End
        **********************************************************************/

        /**********************************************************************
        Function releaseClaim Start
        **********************************************************************/
        function releaseClaim(loanId) {
            return $http.post(RELEASE_CLAIM_URL, loanId).then(function (response) {
                return response.data;
            });
        };
        /**********************************************************************
        Function releaseClaim End
        **********************************************************************/
    }
    /*****************************************************************************************
    dashboardService End
    ******************************************************************************************/
})();
/*****************************************************************************************
dashboardService End
******************************************************************************************/
